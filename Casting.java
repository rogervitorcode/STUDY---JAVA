public class Casting {
    public static void Casting (String[] args){
        /*
        ------------  CASTING -----------------

        ****************(double para int)************
        double resultado = 0.0;
        int resultadoInt = (int) resultado;
        *****************(int para double)*********************
        int meuInt = 10;
        double meuDouble = meuInt;

        System.out.println(resultadoInt);
        System.out.println(meuDouble);

        // USAR SEMPRE a varivel criada para conversao: (meuDouble) e (resultadoInt)
        ******************************( de string para inteiro) -- ( importante o comando Integer )
        String meuString = "10";
        int meuInt2 = Integer.parseInt(meuString);
        System.out.println(meuInt2);

       String minhaString = String.valueOf(meuInt2);
       System.out.println(minhaString);

       (    QUAL A IMPORTANCIA DO CASTING: Pode existir
       arquivos em String que contenha idade, assim a gente precisa
       transformar o String em int )
        */
    }
}
